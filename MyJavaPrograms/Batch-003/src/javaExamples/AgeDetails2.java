package javaExamples;

public class AgeDetails2 {
public static void main(String args[])
{
	int age=10;
	if(age==15)
	{
		System.out.println("He is in Inter standard");
	}
	else if(age==10)
	{
		System.out.println("He is 5th standard");
	}
	else if(age==5)
	{
		System.out.println("He is in Nursery");
	}
	else
		System.out.println("He is doing degree");
	
	}
}
